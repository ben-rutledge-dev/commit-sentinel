#!/usr/bin/env node
'use strict';

/**
 * commit-sentinel-hook
 * Called by git as the commit-msg hook.
 * Usage: node hook.js <path-to-commit-msg-file>
 */

const fs = require('node:fs');
const readline = require('node:readline');
const { execSync } = require('node:child_process');
const { CommitSentinel } = require('../dist/index');
const { loadConfig } = require('../dist/config-loader');

// Try to use chalk
let chalk;
try { chalk = require('chalk'); } catch { chalk = null; }

const msgFile = process.argv[2];

if (!msgFile) {
  console.error('commit-sentinel: no commit message file provided');
  process.exit(1);
}

const rawMessage = fs.readFileSync(msgFile, 'utf8');

// Strip comment lines (lines starting with #)
const cleanMessage = rawMessage
  .split('\n')
  .filter(line => !line.startsWith('#'))
  .join('\n')
  .trim();

if (!cleanMessage) {
  console.error('commit-sentinel: empty commit message');
  process.exit(1);
}

const config = loadConfig();
const sentinel = new CommitSentinel(config);

let blocked  = false;
let commitWarning = null;

// --- Scope (file-isolation) validation ---
try {
  const stagedFiles = execSync('git diff --cached --name-only', { encoding: 'utf8' })
    .trim()
    .split('\n')
    .filter(Boolean);

  if (stagedFiles.length > 0) {
    const scopeResult = sentinel.validateScope(stagedFiles);
    if (!scopeResult.valid) {
      printReport(sentinel.formatScope(stagedFiles, scopeResult));
      if (scopeResult.enforced) blocked = true;
    }
  }
} catch {
  // Could not determine staged files — skip scope checks
}

// --- Commit message validation ---
const commitResult = sentinel.validateCommit(cleanMessage);
if (!commitResult.valid) {
  printReport(sentinel.formatCommit(cleanMessage, commitResult));
  if (commitResult.enforced) {
    blocked = true;
  }

  // Extract the suggested message from suggestions (format: Try: "...")
  const subject = cleanMessage.split('\n')[0].trim();
  const suggestedMsg = extractSuggestion(commitResult.suggestions, subject);

  commitWarning = { result: commitResult, subject, suggestedMsg };
}

// --- Branch name validation ---
try {
  const branchName = execSync('git rev-parse --abbrev-ref HEAD', { encoding: 'utf8' }).trim();
  if (branchName && branchName !== 'HEAD') {
    const branchResult = sentinel.validateBranch(branchName);
    if (!branchResult.valid) {
      printReport(sentinel.formatBranch(branchName, branchResult));
      if (branchResult.enforced) blocked = true;
    }
  }
} catch {
  // Could not determine branch name — skip branch checks
}

// If any enforced check failed and there's no commit warning to interactively resolve, block
if (blocked && !commitWarning) {
  process.exit(1);
}

// If there's a commit issue (enforced or not), show interactive options
if (commitWarning) {
  resolveCommitInteractively(commitWarning).then(action => {
    if (action.type === 'commit') {
      // Rewrite the commit message file with the resolved message
      const newContent = rawMessage.replace(commitWarning.subject, action.message);
      fs.writeFileSync(msgFile, newContent);
      if (action.message !== commitWarning.subject) {
        const msg = chalk
          ? `\n  ${chalk.green('✔')}  Commit message updated to: ${chalk.cyan(action.message)}\n`
          : `\n  ✔  Commit message updated to: ${action.message}\n`;
        process.stderr.write(msg);
      }
      process.exit(0);
    } else {
      process.exit(1);
    }
  }).catch(() => {
    // No TTY (CI) — let the commit through if not enforced, block if enforced
    process.exit(blocked ? 1 : 0);
  });
} else {
  process.exit(0);
}

/**
 * Extract the best suggested commit message from the suggestions array.
 * Looks for entries matching `Try: "..."` and returns the quoted content.
 */
function extractSuggestion(suggestions, originalSubject) {
  for (const s of suggestions) {
    const match = s.match(/^Try: "(.+)"$/);
    if (match && match[1] !== originalSubject) {
      return match[1];
    }
  }
  return null;
}

/**
 * Check if a message has missing required-pattern errors.
 * Returns the error messages for required-pattern failures.
 */
function getMissingPatternErrors(result) {
  const requiredPatterns = config.commits?.requiredPatterns ?? [];
  if (requiredPatterns.length === 0) return [];

  // Match errors that were generated by requiredPatterns
  return result.errors.filter(e => {
    return requiredPatterns.some(rp => {
      const defaultMsg = `Subject must match required pattern: ${rp.pattern}`;
      return e === defaultMsg || (rp.message && e === rp.message);
    });
  });
}

/**
 * Open a TTY output stream for interactive prompts.
 * Returns { ttyOut } or throws if no TTY.
 */
function openTTY() {
  const ttyOut = fs.createWriteStream('/dev/tty');
  return { ttyOut };
}

/**
 * Close a TTY handle.
 */
function closeTTY({ ttyOut }) {
  ttyOut.destroy();
}

/**
 * Ask the user a question on the TTY and return their answer.
 * Creates a fresh input stream and readline per call to avoid
 * fs.ReadStream pause/resume issues that cause double-Enter.
 */
function askQuestion(tty, question) {
  return new Promise((resolve) => {
    const fd = fs.openSync('/dev/tty', 'r');
    const ttyIn = new fs.ReadStream(null, { fd, autoClose: true });
    ttyIn.setEncoding('utf8');
    const rl = readline.createInterface({ input: ttyIn, output: tty.ttyOut, terminal: false });
    rl.question(question, (answer) => {
      rl.close();
      ttyIn.destroy();
      resolve((answer || '').trim());
    });
  });
}

/**
 * Write text to the TTY output (bypassing readline).
 */
function ttyWrite(tty, text) {
  tty.ttyOut.write(text);
}

/**
 * Interactively resolve a commit message that failed validation.
 * - Re-validates suggestions before offering them
 * - Prompts for missing required patterns (e.g. ticket numbers)
 * - Allows entering a custom commit message
 * Returns: { type: 'commit', message } | { type: 'abort' }
 */
async function resolveCommitInteractively({ result, subject, suggestedMsg }) {
  let tty;
  try {
    tty = openTTY();
  } catch {
    throw new Error('No TTY available');
  }

  try {
    // Start with the best candidate message (suggestion or original)
    let candidateMsg = suggestedMsg || subject;

    // Check if the candidate passes validation
    let candidateResult = sentinel.validateCommit(candidateMsg);
    let candidateValid = candidateResult.valid;

    // If the candidate has missing required-pattern errors, prompt for each
    if (!candidateValid) {
      const requiredPatterns = config.commits?.requiredPatterns ?? [];
      if (requiredPatterns.length > 0) {
        const patternErrors = getMissingPatternErrors(candidateResult);
        if (patternErrors.length > 0) {
          // Prompt user for the missing reference
          ttyWrite(tty, '\n');
          for (const rp of requiredPatterns) {
            const regex = new RegExp(rp.pattern);
            if (!regex.test(candidateMsg)) {
              const errorMsg = rp.message || `Subject must match pattern: ${rp.pattern}`;
              const prompt = chalk
                ? `  ${chalk.yellow('?')}  ${errorMsg}\n     Enter the reference to include (or leave blank to skip): `
                : `  ?  ${errorMsg}\n     Enter the reference to include (or leave blank to skip): `;

              const answer = await askQuestion(tty, prompt);
              if (answer) {
                // Prepend the reference to the candidate message
                candidateMsg = `${answer} ${candidateMsg}`;
              }
            }
          }

          // Re-validate after adding references
          candidateResult = sentinel.validateCommit(candidateMsg);
          candidateValid = candidateResult.valid;
        }
      }
    }

    // Build options
    const options = [];

    // Only offer the suggestion if it passes full validation
    if (candidateValid && candidateMsg !== subject) {
      const label = chalk
        ? `Commit with suggested message ${chalk.green(`"${candidateMsg}"`)}`
        : `Commit with suggested message "${candidateMsg}"`;
      options.push({ label, action: 'suggested', message: candidateMsg });
    }

    // Always offer the option to enter a custom message
    {
      const label = chalk
        ? chalk.blue('Enter a new commit message')
        : 'Enter a new commit message';
      options.push({ label, action: 'custom' });
    }

    if (!result.enforced) {
      const label = chalk
        ? `Commit with original message ${chalk.yellow(`"${subject}"`)}`
        : `Commit with original message "${subject}"`;
      options.push({ label, action: 'original', message: subject });
    }

    options.push({
      label: chalk ? chalk.red('Abort commit') : 'Abort commit',
      action: 'abort',
    });

    // Print options
    ttyWrite(tty, '\n');
    options.forEach((opt, i) => {
      const num = chalk ? chalk.cyan(`${i + 1}`) : `${i + 1}`;
      ttyWrite(tty, `  ${num}) ${opt.label}\n`);
    });
    ttyWrite(tty, '\n');

    const choicePrompt = chalk
      ? `  ${chalk.yellow('?')}  Choose an option ${chalk.gray(`[1-${options.length}]`)}: `
      : `  ?  Choose an option [1-${options.length}]: `;

    const answer = await askQuestion(tty, choicePrompt);
    const choice = parseInt(answer, 10);

    if (choice >= 1 && choice <= options.length) {
      const selected = options[choice - 1];

      if (selected.action === 'suggested') {
        closeTTY(tty);
        return { type: 'commit', message: selected.message };
      }

      if (selected.action === 'original') {
        closeTTY(tty);
        return { type: 'commit', message: selected.message };
      }

      if (selected.action === 'custom') {
        return await promptCustomMessage(tty, result);
      }

      // abort
      closeTTY(tty);
      return { type: 'abort' };
    }

    // Invalid input — treat as abort
    closeTTY(tty);
    return { type: 'abort' };
  } catch (e) {
    try { closeTTY(tty); } catch {}
    throw e;
  }
}

/**
 * Prompt the user for a custom commit message, validating it in a loop.
 * When rules are not enforced, offers to commit anyway after a failed validation.
 * Returns: { type: 'commit', message } | { type: 'abort' }
 */
async function promptCustomMessage(tty, originalResult) {
  const maxAttempts = 5;
  for (let attempt = 0; attempt < maxAttempts; attempt++) {
    const prompt = chalk
      ? `\n  ${chalk.yellow('?')}  Enter your commit message (or "abort" to cancel): `
      : `\n  ?  Enter your commit message (or "abort" to cancel): `;

    const input = await askQuestion(tty, prompt);

    if (!input || input.toLowerCase() === 'abort') {
      closeTTY(tty);
      return { type: 'abort' };
    }

    // Validate the entered message
    const validation = sentinel.validateCommit(input);
    if (validation.valid) {
      closeTTY(tty);
      return { type: 'commit', message: input };
    }

    // Show errors
    const errMsg = chalk
      ? `\n  ${chalk.red('✗')}  Message still has issues:`
      : `\n  ✗  Message still has issues:`;
    ttyWrite(tty, errMsg + '\n');
    validation.errors.forEach(e => {
      const line = chalk ? `     ${chalk.red('•')} ${e}` : `     • ${e}`;
      ttyWrite(tty, line + '\n');
    });
    if (validation.suggestions.length > 0) {
      const sugLine = chalk ? `     ${chalk.yellow('💡')} ` : '     💡 ';
      validation.suggestions.forEach(s => ttyWrite(tty, sugLine + s + '\n'));
    }

    // If not enforced, offer to commit anyway with this message
    if (!validation.enforced) {
      ttyWrite(tty, '\n');
      const commitAnywayLabel = chalk
        ? `  1) Commit anyway with ${chalk.yellow(`"${input}"`)}`
        : `  1) Commit anyway with "${input}"`;
      const retryLabel = chalk
        ? `  2) ${chalk.blue('Enter a different message')}`
        : '  2) Enter a different message';
      const abortLabel = chalk
        ? `  3) ${chalk.red('Abort commit')}`
        : '  3) Abort commit';
      ttyWrite(tty, commitAnywayLabel + '\n');
      ttyWrite(tty, retryLabel + '\n');
      ttyWrite(tty, abortLabel + '\n\n');

      const choicePrompt = chalk
        ? `  ${chalk.yellow('?')}  Choose an option ${chalk.gray('[1-3]')}: `
        : '  ?  Choose an option [1-3]: ';

      const answer = await askQuestion(tty, choicePrompt);
      const choice = parseInt(answer, 10);

      if (choice === 1) {
        closeTTY(tty);
        return { type: 'commit', message: input };
      } else if (choice === 3) {
        closeTTY(tty);
        return { type: 'abort' };
      }
      // choice === 2 or invalid → loop to re-enter
      continue;
    }
  }

  const msg = chalk
    ? `\n  ${chalk.red('Too many attempts.')} Aborting.\n`
    : '\n  Too many attempts. Aborting.\n';
  ttyWrite(tty, msg);
  closeTTY(tty);
  return { type: 'abort' };
}

function printReport(report) {
  if (chalk) {
    try {
      const coloured = report
        .replace(/❌/g, chalk.red('❌'))
        .replace(/⚠️/g, chalk.yellow('⚠️'))
        .replace(/✅/g, chalk.green('✅'))
        .replace(/💡/g, chalk.yellow('💡'))
        .replace(/• (.+)/g, (_, m) => `• ${chalk.red(m)}`)
        .replace(/Try: "(.+)"/g, (_, m) => `Try: "${chalk.green(m)}"`);
      process.stderr.write(coloured + '\n');
      return;
    } catch { /* fall through */ }
  }
  process.stderr.write(report + '\n');
}
